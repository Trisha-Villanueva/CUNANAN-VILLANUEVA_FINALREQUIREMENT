    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        //code to get input of user into vrbls user and pwd
            String user = txtUsername.getText();
            String pwd = new String(txtPassword.getPassword());
        
        try {
            //statements to extract data from db and execute mysql query
            pst = con.prepareStatement("SELECT * FROM user_tbl");
            rs = pst.executeQuery();

            //while loop to check data in table one by one
            while (rs.next()){
                //statements to get the data in user_tbl
                String username = rs.getString("username");
                String password = rs.getString("password");
                
                //if statement to check if username and password match w db
                if (user.equals(username) && pwd.equals(password)){
                    new mainForm().setVisible(true);
                    this.setVisible(false);
                    
                }else{
                //error message
                JOptionPane.showMessageDialog(this, "Incorrect username or password.");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(loginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

